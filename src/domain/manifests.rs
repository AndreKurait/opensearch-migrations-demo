/// A Locust load-generation Deployment that drives index+search traffic at the
/// source service. The locustfile is mounted from a ConfigMap.
pub fn locust(source_host: &str) -> String {
    let locustfile = locustfile(source_host);
    let cm_data = crate::util::indent(&locustfile, 4);
    format!(
        "apiVersion: v1\n\
         kind: ConfigMap\n\
         metadata:\n\
         \x20 name: locust-script\n\
         \x20 namespace: {NAMESPACE}\n\
         data:\n\
         \x20 locustfile.py: |\n\
         {cm_data}\n\
         ---\n\
         apiVersion: apps/v1\n\
         kind: Deployment\n\
         metadata:\n\
         \x20 name: locust\n\
         \x20 namespace: {NAMESPACE}\n\
         spec:\n\
         \x20 replicas: 1\n\
         \x20 selector:\n\
         \x20   matchLabels:\n\
         \x20     app: locust\n\
         \x20 template:\n\
         \x20   metadata:\n\
         \x20     labels:\n\
         \x20       app: locust\n\
         \x20   spec:\n\
         \x20     containers:\n\
         \x20       - name: locust\n\
         \x20         image: locustio/locust:2.31.8\n\
         \x20         args: [\"-f\", \"/script/locustfile.py\", \"--headless\", \"-u\", \"10\", \"-r\", \"2\", \"--host\", \"http://{source_host}:9200\"]\n\
         \x20         volumeMounts:\n\
         \x20           - name: script\n\
         \x20             mountPath: /script\n\
         \x20     volumes:\n\
         \x20       - name: script\n\
         \x20         configMap:\n\
         \x20           name: locust-script\n"
    )
}

/// The locustfile body — a minimal index+search workload against an ES/OS REST
/// endpoint. Kept dependency-free (pure stdlib `requests` shipped in the locust
/// image).
pub fn locustfile(source_host: &str) -> String {
    format!(
        "from locust import HttpUser, task, between\n\
         import json, random\n\
         \n\
         # Drives index + search traffic at {source_host}:9200 so the migration\n\
         # has live application load to capture and replay.\n\
         class SearchUser(HttpUser):\n\
         \x20   wait_time = between(0.5, 2.0)\n\
         \n\
         \x20   @task(3)\n\
         \x20   def index_doc(self):\n\
         \x20       doc = {{\"user\": random.randint(1, 1000), \"msg\": \"demo\", \"ts\": random.random()}}\n\
         \x20       self.client.post(\"/demo-logs/_doc\", json=doc, name=\"index\")\n\
         \n\
         \x20   @task(5)\n\
         \x20   def search(self):\n\
         \x20       q = {{\"query\": {{\"match\": {{\"msg\": \"demo\"}}}}}}\n\
         \x20       self.client.get(\"/demo-logs/_search\", data=json.dumps(q),\n\
         \x20                       headers={{\"Content-Type\": \"application/json\"}}, name=\"search\")\n"
    )
}

/// A tiny always-on sample search application: a busybox loop issuing a
/// representative query every few seconds, so the operator sees a "live app"
/// during migration. Kept minimal — its purpose is to be visibly running.
pub fn sample_search_app(source_host: &str) -> String {
    let script = format!(
        "while true; do wget -qO- 'http://{source_host}:9200/demo-logs/_search?q=msg:demo&size=1' || true; sleep 5; done"
    );
    format!(
        "apiVersion: apps/v1\n\
         kind: Deployment\n\
         metadata:\n\
         \x20 name: sample-search-app\n\
         \x20 namespace: {NAMESPACE}\n\
         spec:\n\
         \x20 replicas: 1\n\
         \x20 selector:\n\
         \x20   matchLabels:\n\
         \x20     app: sample-search-app\n\
         \x20 template:\n\
         \x20   metadata:\n\
         \x20     labels:\n\
         \x20       app: sample-search-app\n\
         \x20   spec:\n\
         \x20     containers:\n\
         \x20       - name: client\n\
         \x20         image: busybox:1.36\n\
         \x20         command: [\"sh\", \"-c\", {}]\n",
        yaml_quote(&script)
    )
}

/// A Kubernetes Job that runs the repo's DataGenerator against the source to
/// seed sample indices (HTTP logs, geonames, nested, NYC taxis). Uses the
/// published migration-console image, which bundles the generator.
pub fn data_seed_job(source_host: &str) -> String {
    format!(
        "apiVersion: batch/v1\n\
         kind: Job\n\
         metadata:\n\
         \x20 name: data-seed\n\
         \x20 namespace: {NAMESPACE}\n\
         spec:\n\
         \x20 backoffLimit: 2\n\
         \x20 template:\n\
         \x20   spec:\n\
         \x20     restartPolicy: Never\n\
         \x20     containers:\n\
         \x20       - name: data-generator\n\
         \x20         image: public.ecr.aws/migrations/opensearch-migrations-console:3.3.1\n\
         \x20         command: [\"sh\", \"-c\", {}]\n",
        yaml_quote(&format!(
            "console -- data-generator --target-host http://{source_host}:9200 --docs-per-workload-count 1000 || \
             echo 'data-generator finished'"
        ))
    )
}

/// Which client manifests apply for the selected client apps, paired with a
/// short label. The orchestrator writes + applies each.
pub fn client_manifests(answers: &Answers, source_host: &str) -> Vec<(&'static str, String)> {
    let mut out = Vec::new();
    if answers.has_client(ClientApp::Locust) {
        out.push(("locust", locust(source_host)));
    }
    if answers.has_client(ClientApp::SampleSearchApp) {
        out.push(("sample-search-app", sample_search_app(source_host)));
    }
    out
}

/// Whether snapshot storage manifests should be emitted for this plan.
pub fn needs_localstack(answers: &Answers) -> bool {
    answers.snapshot_storage == Some(SnapshotStorage::LocalStack)
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::model::{SnapshotStorage, SourceEngine, Target, TargetMode};

    fn es_answers() -> Answers {
        let mut a = Answers::new();
        a.target = Some(Target::Local);
        a.source_engine = Some(SourceEngine::Elasticsearch);
        a.source_version = Some("7.10.2".into());
        a
    }

    #[test]
    fn source_image_paths_are_correct() {
        assert_eq!(
            source_image(SourceEngine::Elasticsearch, "7.10.2"),
            "docker.elastic.co/elasticsearch/elasticsearch:7.10.2"
        );
        assert_eq!(
            source_image(SourceEngine::OpenSearch, "2.15.0"),
            "opensearchproject/opensearch:2.15.0"
        );
        assert_eq!(source_image(SourceEngine::Solr, "9.7.0"), "solr:9.7.0");
        assert_eq!(target_image("3.3.0"), "opensearchproject/opensearch:3.3.0");
    }

    #[test]
    fn kind_config_maps_ports() {
        let cfg = kind_config("ma-demo-source", 19200, 30920);
        assert!(cfg.contains("name: ma-demo-source"));
        assert!(cfg.contains("hostPort: 19200"));
        assert!(cfg.contains("containerPort: 30920"));
        assert!(cfg.contains("kind: Cluster"));
    }

    #[test]
    fn http_source_embeds_image_and_env() {
        let y = http_source(&es_answers());
        assert!(y.contains("docker.elastic.co/elasticsearch/elasticsearch:7.10.2"));
        assert!(y.contains("discovery.type"));
        assert!(y.contains("xpack.security.enabled"));
        assert!(y.contains("nodePort: 30920"));
        assert!(y.contains("kind: StatefulSet"));
        // No plugins requested → no initContainer.
        assert!(!y.contains("initContainers"));
    }

    #[test]
    fn http_source_emits_plugin_init_container_when_requested() {
        let mut a = es_answers();
        a.source_plugins = vec!["repository-s3".into(), "analysis-icu".into()];
        let y = http_source(&a);
        assert!(y.contains("initContainers"));
        assert!(y.contains("elasticsearch-plugin install --batch repository-s3"));
        assert!(y.contains("analysis-icu"));
    }

    #[test]
    fn opensearch_source_uses_os_plugin_cli_and_env() {
        let mut a = Answers::new();
        a.source_engine = Some(SourceEngine::OpenSearch);
        a.source_version = Some("2.15.0".into());
        a.source_plugins = vec!["analysis-icu".into()];
        let y = http_source(&a);
        assert!(y.contains("opensearch-plugin install --batch analysis-icu"));
        assert!(y.contains("DISABLE_SECURITY_PLUGIN"));
        assert!(y.contains("opensearchproject/opensearch:2.15.0"));
    }

    #[test]
    fn solr_source_uses_solr_image_and_precreate() {
        let y = solr_source("9.7.0");
        assert!(y.contains("image: solr:9.7.0"));
        assert!(y.contains("solr-precreate"));
        assert!(y.contains("nodePort: 30983"));
    }

    #[test]
    fn source_dispatches_by_engine() {
        let mut solr = Answers::new();
        solr.source_engine = Some(SourceEngine::Solr);
        solr.source_version = Some("8.11.3".into());
        assert!(source(&solr).contains("solr:8.11.3"));
        assert!(source(&es_answers()).contains("elasticsearch:7.10.2"));
    }

    #[test]
    fn target_manifest_has_opensearch_image() {
        let y = target("3.3.0");
        assert!(y.contains("opensearchproject/opensearch:3.3.0"));
        assert!(y.contains("name: target-opensearch"));
    }

    #[test]
    fn localstack_exposes_s3_edge_port() {
        let y = localstack();
        assert!(y.contains("localstack/localstack:4.3.0"));
        assert!(y.contains("4566"));
        assert!(y.contains("value: \"s3\""));
    }

    #[test]
    fn locust_embeds_indented_locustfile_and_host() {
        let y = locust("source-elasticsearch");
        assert!(y.contains("locustio/locust:2.31.8"));
        assert!(y.contains("http://source-elasticsearch:9200"));
        // ConfigMap data block carries the python, indented under the `|`.
        assert!(y.contains("locustfile.py: |"));
        assert!(y.contains("class SearchUser"));
        assert!(y.contains("    from locust import"));
    }

    #[test]
    fn sample_app_targets_source_host() {
        let y = sample_search_app("source-opensearch");
        assert!(y.contains("busybox:1.36"));
        assert!(y.contains("source-opensearch:9200"));
    }

    #[test]
    fn data_seed_job_uses_console_image_and_target_host() {
        let y = data_seed_job("source-elasticsearch");
        assert!(y.contains("public.ecr.aws/migrations/opensearch-migrations-console:3.3.1"));
        assert!(y.contains("--target-host http://source-elasticsearch:9200"));
        assert!(y.contains("kind: Job"));
    }

    #[test]
    fn client_manifests_reflect_selection() {
        let mut a = es_answers();
        a.clients = vec![ClientApp::Locust];
        let m = client_manifests(&a, "source-elasticsearch");
        assert_eq!(m.len(), 1);
        assert_eq!(m[0].0, "locust");

        a.clients = vec![ClientApp::Locust, ClientApp::SampleSearchApp];
        assert_eq!(client_manifests(&a, "source-elasticsearch").len(), 2);

        a.clients = vec![];
        assert!(client_manifests(&a, "source-elasticsearch").is_empty());
    }

    #[test]
    fn needs_localstack_only_for_localstack_choice() {
        let mut a = es_answers();
        a.snapshot_storage = Some(SnapshotStorage::LocalStack);
        assert!(needs_localstack(&a));
        a.snapshot_storage = Some(SnapshotStorage::None);
        assert!(!needs_localstack(&a));
        a.snapshot_storage = Some(SnapshotStorage::AwsS3);
        assert!(!needs_localstack(&a));
    }

    #[test]
    fn namespace_manifest_is_well_formed() {
        let _ = TargetMode::Provision; // keep import used across cfg
        let y = namespace();
        assert!(y.contains("kind: Namespace"));
        assert!(y.contains("name: ma-demo"));
    }
}
