//! Pure manifest + KIND-config emitters.
//!
//! Every function here is a pure projection of [`Answers`] (or a small typed
//! input) to a YAML/string artifact — no I/O. The provisioning orchestrator
//! ([`crate::command::app`]) writes these to the workspace and `kubectl apply`s
//! them through the [`CommandRunner`] seam. Keeping the emitters pure means the
//! exact rendered YAML is asserted in unit tests, the same discipline the
//! migration-assistant CLI uses for its helm/manifest builders.
//!
//! Image references intentionally use public registries (docker.elastic.co,
//! docker.io/opensearchproject, public.ecr.aws) pinned to the versions the
//! Migration Assistant 3.3.1 release targets.

use crate::model::{Answers, ClientApp, SnapshotStorage, SourceEngine};
use crate::util::yaml_quote;

/// The Kubernetes namespace every harness-provisioned workload lives in.
pub const NAMESPACE: &str = "ma-demo";

/// Resolve the container image for a source engine + version.
pub fn source_image(engine: SourceEngine, version: &str) -> String {
    match engine {
        SourceEngine::Elasticsearch => {
            format!("docker.elastic.co/elasticsearch/elasticsearch:{version}")
        }
        SourceEngine::OpenSearch => format!("opensearchproject/opensearch:{version}"),
        SourceEngine::Solr => format!("solr:{version}"),
    }
}

/// Resolve the target OpenSearch image for a version.
pub fn target_image(version: &str) -> String {
    format!("opensearchproject/opensearch:{version}")
}

/// A KIND cluster config that maps a NodePort to a host port, so the operator
/// (and the Migration Assistant) can reach the cluster from the host. `role`
/// distinguishes source from target; `host_port` is the port published on the
/// host's loopback.
pub fn kind_config(cluster_name: &str, host_port: u16, node_port: u16) -> String {
    format!(
        "# KIND cluster config for the {cluster_name} role.\n\
         # Generated by migration-assistant-demo — do not edit by hand.\n\
         kind: Cluster\n\
         apiVersion: kind.x-k8s.io/v1alpha4\n\
         name: {cluster_name}\n\
         nodes:\n\
         \x20 - role: control-plane\n\
         \x20   extraPortMappings:\n\
         \x20     - containerPort: {node_port}\n\
         \x20       hostPort: {host_port}\n\
         \x20       protocol: TCP\n"
    )
}

/// The Namespace manifest.
pub fn namespace() -> String {
    format!("apiVersion: v1\nkind: Namespace\nmetadata:\n  name: {NAMESPACE}\n")
}

/// A single-node Elasticsearch/OpenSearch StatefulSet + Service for the source.
///
/// Security is disabled (single-node demo), discovery is single-node, and the
/// requested `plugins` are installed by an initContainer via the engine's
/// plugin CLI when non-empty. The Service exposes a NodePort so a KIND
/// extraPortMapping can publish it to the host.
pub fn http_source(answers: &Answers) -> String {
    let engine = answers
        .source_engine
        .expect("http_source requires an HTTP search engine");
    let version = answers.source_version.as_deref().unwrap_or("latest");
    let image = source_image(engine, version);
    let plugin_cli = match engine {
        SourceEngine::Elasticsearch => "elasticsearch-plugin",
        SourceEngine::OpenSearch => "opensearch-plugin",
        SourceEngine::Solr => unreachable!("solr is not an http source"),
    };
    let (env, name) = match engine {
        SourceEngine::Elasticsearch => (es_env(), "source-elasticsearch"),
        SourceEngine::OpenSearch => (os_env(), "source-opensearch"),
        SourceEngine::Solr => unreachable!(),
    };

    let init = plugin_init_container(plugin_cli, &answers.source_plugins, &image);

    format!(
        "apiVersion: v1\n\
         kind: Service\n\
         metadata:\n\
         \x20 name: {name}\n\
         \x20 namespace: {NAMESPACE}\n\
         spec:\n\
         \x20 type: NodePort\n\
         \x20 selector:\n\
         \x20   app: {name}\n\
         \x20 ports:\n\
         \x20   - name: http\n\
         \x20     port: 9200\n\
         \x20     targetPort: 9200\n\
         \x20     nodePort: 30920\n\
         ---\n\
         apiVersion: apps/v1\n\
         kind: StatefulSet\n\
         metadata:\n\
         \x20 name: {name}\n\
         \x20 namespace: {NAMESPACE}\n\
         spec:\n\
         \x20 serviceName: {name}\n\
         \x20 replicas: 1\n\
         \x20 selector:\n\
         \x20   matchLabels:\n\
         \x20     app: {name}\n\
         \x20 template:\n\
         \x20   metadata:\n\
         \x20     labels:\n\
         \x20       app: {name}\n\
         \x20   spec:\n\
         {init}\
         \x20     containers:\n\
         \x20       - name: {name}\n\
         \x20         image: {image}\n\
         \x20         ports:\n\
         \x20           - containerPort: 9200\n\
         \x20         env:\n\
         {env}\
         \x20         volumeMounts:\n\
         \x20           - name: plugins\n\
         \x20             mountPath: /plugins-extra\n\
         \x20     volumes:\n\
         \x20       - name: plugins\n\
         \x20         emptyDir: {{}}\n",
    )
}

/// The ES single-node env block (security off, single discovery, bounded heap).
fn es_env() -> String {
    [
        ("discovery.type", "single-node"),
        ("xpack.security.enabled", "false"),
        ("ES_JAVA_OPTS", "-Xms512m -Xmx512m"),
    ]
    .iter()
    .map(|(k, v)| env_pair(k, v))
    .collect::<Vec<_>>()
    .join("")
}

/// The OpenSearch single-node env block.
fn os_env() -> String {
    [
        ("discovery.type", "single-node"),
        ("DISABLE_SECURITY_PLUGIN", "true"),
        ("OPENSEARCH_JAVA_OPTS", "-Xms512m -Xmx512m"),
        ("OPENSEARCH_INITIAL_ADMIN_PASSWORD", "Demo_Admin_1234!"),
    ]
    .iter()
    .map(|(k, v)| env_pair(k, v))
    .collect::<Vec<_>>()
    .join("")
}

/// One `env:` list entry (12-space indented to sit under the container).
fn env_pair(name: &str, value: &str) -> String {
    format!(
        "\x20           - name: {name}\n\x20             value: {}\n",
        yaml_quote(value)
    )
}

/// An initContainer that installs each plugin via the engine's plugin CLI into
/// a shared volume, or empty string when no plugins were requested.
fn plugin_init_container(plugin_cli: &str, plugins: &[String], image: &str) -> String {
    if plugins.is_empty() {
        return String::new();
    }
    let installs = plugins
        .iter()
        .map(|p| format!("{plugin_cli} install --batch {p}"))
        .collect::<Vec<_>>()
        .join(" && ");
    format!(
        "\x20     initContainers:\n\
         \x20       - name: install-plugins\n\
         \x20         image: {image}\n\
         \x20         command: [\"sh\", \"-c\", {}]\n",
        yaml_quote(&installs)
    )
}

